import socket


def get_host_ip() -> str:
    """Get the host IP address."""
    try:
        # Connect to a remote address to determine local IP
        with socket.socket(socket.AF_INET, socket.SOCK_DGRAM) as s:
            s.connect(("8.8.8.8", 80))
            host_ip = s.getsockname()[0]
        return host_ip
    except OSError:
        return "localhost"
